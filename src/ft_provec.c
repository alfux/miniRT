#include <miniRT.h>

t_vec	ft_provec(t_vec u, t_vec v)
{
	return (ft_setvec(u.y * v.z - u.z * v.y, u.z * v.x - u.x * v.z,
		u.x * v.y - u.y * v.x));
}
