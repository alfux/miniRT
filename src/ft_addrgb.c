#include <miniRT.h>

t_rgb	ft_addrgb(t_rgb c1, t_rgb c2)
{
	t_rgb	sum;

	sum.r = fmin(255, c1.r + c2.r);
	sum.g = fmin(255, c1.g + c2.g);
	sum.b = fmin(255, c1.b + c2.b);
	return (sum);
}
