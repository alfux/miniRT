#include <miniRT.h>

t_vec	ft_sum_uv(t_vec u, t_vec v)
{
	t_vec	sum;

	sum.x = u.x + v.x;
	sum.y = u.y + v.y;
	sum.z = u.z + v.z;
	return (sum);
}
